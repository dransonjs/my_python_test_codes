

def square(n):
    """
    求平方
    :param n: 一个数
    :return: 一个数的平方
    """
    return n**2


def cube(n):
    """
    求立方
    :param n: 一个数
    :return: 一个数的立方
    """
    return n**3


def num_abs(n):
    """
    求绝对值
    :param n: 一个数
    :return: 一个数的绝对值
    """
    return abs(n)


